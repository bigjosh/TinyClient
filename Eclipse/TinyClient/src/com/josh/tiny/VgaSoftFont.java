	public void saveFont( File filename) throws IOException {
		
		VgaSoftFont v = this;
		
		BufferedWriter w = new BufferedWriter( new FileWriter(filename ) );
		w.write("# This is a custom font file for the TINY client\r\n");
		w.write("# any line that starts with a '#' or ';' is ignored\r\n");
		w.write("#\r\n");
		w.write("# first is the char height in pixels\r\n");
		w.write( Integer.toString( v.height ));
		w.write("\r\n");
		w.write("#\r\n");		
		w.write("# next is the char width in pixels\r\n");
		w.write( Integer.toString( v.width) );
		w.write("\r\n");
		w.write("#\r\n");		
		w.write("# next comes the color pallet\r\n");
		w.write("# there are 16 text colors\r\n");		
		w.write("# list one color per line\r\n");
		w.write("# each color is a 24 bit hex number\r\n");
		w.write("# the 3 bytes are the red, green, and blue values\r\n");		
		w.write("#\r\n");			
		for(int c=0; c < 16; c++ ) {
			w.write( "# "+colornames[c]+"\r\n");
			
			String s = ((Integer.toHexString(v.colors[c])).toUpperCase());
			
			s = "000000".substring( s.length() ) + s;			// pad left
			
			w.write( s +"\r\n");		
		}
		
		
		w.write("#\r\n");				
		w.write("# finally comes the actual pixel data\r\n");
		w.write("# there must be exactly 256 chars\r\n");				
		w.write("# each char should be (height) lines\r\n");
		w.write("# each of those lines should be (width) chars long\r\n");
		w.write("# and on pixel can be X,x,o,O, or *. Anything else is an off pixel.\r\n");		
		w.write("#\r\n");		
		
		for(int c=0;c<256;c++) {


			w.write("# (ASCII "+c+") \r\n");
				
			
			
			for(int y=0; y<v.height; y++ ){
				
				StringBuffer s = new StringBuffer( v.width );
				
				for( int x=0; x<v.width; x++) {
			
					s.append( v.pixels[c][y][x] ? 'X':'.' );
							
				}
				
				
				w.write( s.toString() +"\r\n");				
				
			}
			
			
		}
		
		w.close();
		
	}
	
	public static void main( String args[] ) throws IOException, FontFormatException {

		System.out.println( "Fontfile converter for TinyClient [http://josh.com/tiny]");
		System.out.println( "This program can read various font file formats and create");
		System.out.println( "a .tf file that TinyClient can load.");
		System.out.println( "");

	
		VgaSoftFont v;
		
		switch (args[0].charAt(0)) {
		
			case 'F': {
				
				System.out.println("Reading FontEdit font from "+args[1]+"...");
				
				v = new VgaSoftFont( new File(args[1]) , FontFileType.FontEdit );				
				
			}
			break;
		
			case 'T': {
				
				System.out.println("Reading TrueType font from "+args[1]+"...");
				
				v = new VgaSoftFont( new File(args[1]) , FontFileType.TTF );				
				
			}
			break;
			
			default: {
				
				System.out.println( "Usage: VgaSoftFont format infile outfile");
				
				System.out.println( "");
				System.out.println( "Where infile is the file to read and outfile is the file to create");
				System.out.println( "");
				System.out.println( "Format can be:");
				System.out.println( "F for file created by FONTEDIT.COM (only works for 16 line fonts)");
				System.out.println( "T for TrueType font file");
				System.out.println( "");
				return;
			}
			
		}
		
		
		System.out.println( "Writing "+args[2]+"...");
		
		v.saveFont(new File(args[2]));
		
		System.out.println( "Done.");
		
					
}	
	
	

	//Read a line, skipping lines that start with # for comments
	
	private String rl(BufferedReader br) throws IOException {
		
		String s;
		
		do {
			s= br.readLine();
			
		} while ( s.charAt(0)=='#' || s.charAt(0)==';');
		
		return(s);
	
	}
			
		
	private void loadFromTextFile( File fontfile ) throws IOException {
		
		BufferedReader input = new BufferedReader(new FileReader(fontfile ));
		
		String s;
		
			
		s = rl(input);
		
		
		height = Integer.parseInt(s);
		
		s = rl(input);
		width = Integer.parseInt(s);	
		
		for( int cl = 0 ; cl < 16 ; cl++ ){
			
			s = rl(input);
			
			colors[cl] = Integer.parseInt(s,16);
			
		}
		
		count = 256;
			
		pixels = new boolean[count][height][width];
		
		for( int cl = 0 ; cl < count ; cl++) {
			
			for( int yl = 0 ; yl < height ; yl++) {
				
				s = rl(input);
				
				for ( int xl = 0 ; xl < width ; xl++ ) {
					
					if ( "xX*oO".indexOf( s.charAt(xl) ) > 0) {	
						
						pixels[cl][yl][xl]= true;
						
					}						
			
				}
				
			}
			
			
		}	
		
		input.close();
			
	}
	
	// This just loads a single font file I found on the internet for a nice 5x7 font
	
	private void loadFromDBFile( File fontfile ) throws IOException {
		
		count  = 256;
		height = 8;
		width  = 6;
		
		pixels = new boolean[count][height][width];
				
		BufferedReader input = new BufferedReader(new FileReader(fontfile ));
		
		String s;		
		
		for( int c = 0 ; c < 256 ; c++ ) {
			
			s = rl(input);
			
			if (s==null ) break;
			
			String b[] = s.split("[\\s\\t,h]+");
			
			System.out.println("-"+s+"-");
			
			int bits[] = new int[6];
			
			for( int l=0 ; l < 6 ; l++ ) {
				
				bits[l] = Integer.parseInt( b[l+2], 16);
				
				System.out.println(l+"="+Integer.toBinaryString(bits[l]));
			}
			
			int mask = 0x01;
			
			for( int y = 0 ; y < 8 ; y++ ) {
				
				for( int x = 0; x < 6 ; x++ ) {
				
					if ( (bits[x] & mask )!=0) {
						
						pixels[c][y][x] = true;
					} else {
						pixels[c][y][x] = false;

												
					} 
				}
				
				mask *= 2;

			}
			
				
				
		}
					
		input.close();
		
		colors = defaultColors;
		
		
	}	
	
	
	private void loadFromTTFFile( File file ) throws IOException, FontFormatException {
				
		Font font = Font.createFont( Font.TRUETYPE_FONT ,  file );
		
		System.out.println("loaded "+ font.getFamily() );
		
		
		font = font.deriveFont((float) 23 );
		
		// First figure out how big the biggest char is
				
		FontRenderContext frc = new FontRenderContext( null , false , false );
		
		Rectangle2D r= font.getMaxCharBounds(frc);
		
		count = 256;
		
		width = (int) Math.ceil( r.getWidth() );
		height = (int) Math.ceil( r.getHeight() );
		
		pixels = new boolean[count][height][width];
		
		
		BufferedImage bufferedImage = new BufferedImage(  width , height , BufferedImage.TYPE_BYTE_BINARY  ) ;
		
		byte[] b = { (byte) 65 };
			
		Graphics g = bufferedImage.getGraphics();
		
		g.setFont(font);
		g.setColor(Color.WHITE);
		
		
		for( int c=0; c <256; c++ ) {
			
			if ( font.canDisplay((char) c )) {
			
				g.clearRect( 0 , 0 , width , height );
				
				b[0] = (byte) c;
				
				int startline =  height - g.getFontMetrics().getDescent();				
							
				g.drawBytes( b , 0, 1 , 0 , startline );
				
				for( int y=0; y< height; y++ ){
					
					for( int x=0; x <width ;x++) {
							
						if ( (bufferedImage.getRGB(x, y) & 0x00ffffff) != 0 ) {						
							pixels[c][y][x] = true;
						} else {
							pixels[c][y][x] = false;
	
						}
						
					}

				}
			}
		}
		
		colors = defaultColors;
		
	}
	

	// Reads in a file created by FONTEXE.EXE but only works for standard 16 line fonts
	
	private void loadFromFontEditFile( File file ) throws IOException {
		
		count  = 256;
		height = 16;
		width  = 8;
		
		pixels = new boolean[count][height][width];
			
		byte[] inLine = new byte[1];		// One line of pixels

		StringBuffer outLine = new StringBuffer(8);
		
		RandomAccessFile f = new RandomAccessFile( file , "r" );
				
		f.seek( 0x163 );		// Jump to the start of the font data
		
		int z = 16;			// not sure why but fontfile starts at ASCII 16 and then wraps
		
		for( int c = 0 ; c < 256 ; c++ ) {
								
			for( int y=0; y < 16 ; y++ ) {
				
				f.read( inLine );
	
				outLine.setLength(0);
				
				for( int x = 0; x < 8 ; x++ ) {
					
					int b = inLine[0];		// The byte
					
					int m = 128 >> x ;		// The mask
					
					if ( (b & m) != 0 ) {
						
						pixels[z][y][x] = true;
						
						
					} else {
						
						pixels[z][y][x] = false;
						
					}
					
				}
				

				
			}
			
			z++;
			
			if (z==256) z=0;
					
			
		}
		
		f.close();
		
		colors = defaultColors;
		
	}
	
	public VgaSoftFont( File fontfile ) throws IOException {
		
		if (fontfile==null) {
			
			loadDefaultFont();
			
		} else {
		
			loadFromTextFile(fontfile);
			
		}

	}
	
	public VgaSoftFont() {
				
		loadDefaultFont();
	
	}
	
	static enum FontFileType { Text, DBFont , FontEdit , TTF };
	
	public VgaSoftFont( File fontfile , FontFileType fontFileType ) throws FileNotFoundException, IOException, FontFormatException {
		
		switch(fontFileType){
		
				case Text:{
					loadFromTextFile( fontfile );
				};
				break;
				
				case DBFont: {
					
					loadFromDBFile(fontfile);
					
					
				};
				break;
				
				case FontEdit: {
					
					loadFromFontEditFile(fontfile);
					
				};
				break;
			
				case TTF: {
					
					loadFromTTFFile(fontfile);
					
				};
				break;
		}
		
	}
	
